    public void Agregardatos(){
    
      
        try {
         
           
           
            String SQL = "INSERT into usuarios ( nom_usu,appat_usu,apmat_usu,nik_usu,correo_usu,pass_usu) value (?,?,?,?,?,?) ";
            
            PreparedStatement ps = con.prepareStatement(SQL);
       
            ps.setString(1, txtnombre.getText());
            ps.setString(2, txtpaterno.getText());
            ps.setString(3, txtmaterno.getText());
            ps.setString(4, txtusuario.getText());
            ps.setString(5, txtcorreo.getText());
            ps.setString(6, txtpassword.getText());
            ps.execute();
            JOptionPane.showMessageDialog(null,"Registro Exitoso");
        
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Registro Fallido: " + e.getMessage());
           
        }
    }
    
